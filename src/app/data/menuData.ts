// Helper functions
export function getItemsByCategory(categoryId: string): MenuItem[] {
  if (categoryId === 'all') return MENU_ITEMS;
  return MENU_ITEMS.filter(item => item.category === categoryId);
}

export function searchItems(query: string): MenuItem[] {
  const q = query.toLowerCase();
  return MENU_ITEMS.filter(item =>
    item.name.toLowerCase().includes(q) ||
    item.desc.toLowerCase().includes(q) ||
    item.ingredients?.some(i => i.toLowerCase().includes(q))
  );
}

export function getItemById(id: string): MenuItem | undefined {
  return MENU_ITEMS.find(item => item.id === id);
}

export function getRecommendedItems(currentId: string, limit = 4): MenuItem[] {
  const current = getItemById(currentId);
  if (!current) return MENU_ITEMS.slice(0, limit);
  return MENU_ITEMS
    .filter(item => item.id !== currentId && item.category === current.category)
    .slice(0, limit);
}

